    override func didReceiveMemoryWarning() {
        super.didReceiveMemoryWarning()
    }
    
    func switchCell(switchCell: SwitchCell, didChangeValue value: Bool) {
        let indexPath = tableView.indexPathForCell(switchCell)!
        print("filters view controller got the switch event")
        switchStates[indexPath.row] = value
        let filterString = filterData[indexPath.section][0] as! String
        if allStates[filterString] != nil {
            allStates[filterString]?[indexPath.row] = value
        } else {
            allStates[filterString] = [indexPath.row: value]
        }
        //allStates[filterData[indexPath.section][0] as! String] ||= []
        
        print(allStates)
    }
    
    @IBAction func onSearch(sender: AnyObject) {
        dismissViewControllerAnimated(true, completion: nil)
        var filters = [String:AnyObject]()
        
        var selectedCategories = [String]()
        var deals = [Bool]()
        var radius = [String]()
        var sortBy = [String]()
        
        for (section,states) in allStates {
            for (row,isSelected) in states {
                switch section {
                case "Deals":
                    deals.append(isSelected)
                case "Radius":
                    radius.append((filterData[1][1][row])! as! String)
                case "Sort By":
                    sortBy.append(filterData[2][1][row] as! String)
                case "Categories":
                    if isSelected == true {
                        selectedCategories.append(categories[row]["code"]!)
                    }
                default:
                    print("blah")
                }
            }
            if selectedCategories.count > 0 {
                filters["categories"] = selectedCategories
            }
            if deals.count > 0 {
                filters["deals"] = deals[0]
            } else {
                filters["deals"] = false
            }
            if radius.count > 0 {
                filters["radius"] = radius[0]
            } else {
                filters["radius"] = "auto"
            }
            if sortBy.count > 0 {
                filters["sortBy"] = sortBy[0]
            } else {
                filters["sortBy"] = "Distance"
            }
        }
        delegate?.filtersViewController?(self, didUpdateFilters: filters)
    }
    
    @IBAction func onCancel(sender: AnyObject) {
        dismissViewControllerAnimated(true, completion: nil)
    }
    
    
    //Table View
    
    func tableView(tableView: UITableView, cellForRowAtIndexPath indexPath: NSIndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCellWithIdentifier("SwitchCell", forIndexPath: indexPath) as! SwitchCell
        cell.delegate = self
        //print("section:\(indexPath.section), row:\(indexPath.row)")
        
        cell.onSwitch.on = allStates[filterData[indexPath.section][0] as! String]?[indexPath.row] ?? false
        
        
        switch indexPath.section {
        case 0,1,2:
            cell.switchLabel.text = filterData[indexPath.section][1][indexPath.row] as? String ?? "label"
        case 3:
            cell.switchLabel.text = categories[indexPath.row]["name"]
            //cell.onSwitch.on = switchStates[indexPath.row] ?? false
            //print(indexPath.section)
        default:
            print("blah")
        }
        
        return cell
    }
    
    func tableView(tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return filterData[section][1].count
        //return categories.count
    }
    
    func numberOfSectionsInTableView(tableView: UITableView) -> Int {
        return filterData.count
        //return categories.count
    }
    
    //Table View HEADER
    func tableView(tableView: UITableView, viewForHeaderInSection section: Int) -> UIView? {
        let header = tableView.dequeueReusableHeaderFooterViewWithIdentifier(HeaderViewIdentifier) as UITableViewHeaderFooterView?
        if section == 0 {
            return nil
        } else {
            header!.textLabel!.text = filterData[section][0] as? String
        }
        
        return header
    }
    
    func tableView(tableView: UITableView, heightForHeaderInSection section: Int) -> CGFloat {
        if section == 0 {
            return 0
        } else {
            return 30
        }
    }

